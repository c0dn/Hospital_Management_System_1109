package medical;

/**
 * This class represents a healthcare provider such as a hospital or clinic.
 * It stores information such as the Healthcare Provider Name and Code.
 */

public class HealthcareProvider {
    public String healthcareProviderName;
    public String hciCode;

    public String getHealthcareProviderName() { return healthcareProviderName; }

    public String getHciCode() { return hciCode; }
}
